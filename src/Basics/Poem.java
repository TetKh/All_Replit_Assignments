package Basics;

public class Poem {

    public static void main(String[] args) {
        System.out.println("Please compile, please compile,\n" +
                "It would surely make me smile,\n" +
                "When I glance at the clock,\n" +
                "And thirty minutes has gone by non-stop,\n" +
                "I think that I've won, that I've beaten the odds,\n" +
                "But alas it is not the day, says the evil programming gods,\n" +
                "As this is all a ruse, just one big long deception,\n" +
                "Since my terminal froze ten minutes ago, from Runtime Exception");
    }
}
