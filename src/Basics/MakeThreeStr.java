package Basics;

public class MakeThreeStr {

    public static String makeThreeStr(String word) {

        System.out.print(word + word + word);
        return "";

    }

    public static void main(String[] args) {

        System.out.println(makeThreeStr("hello"));
        System.out.println(makeThreeStr("DuoTech"));

    }

}



