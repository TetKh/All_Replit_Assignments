package Loops;

public class Class_DoWhileDemo {

    public static void main(String[] args) {




        int num = 300;

        while(num < 200){
            System.out.println(num);
            num++;
        }



        do {
            System.out.println("Body");
            // body will be executed at least once
        } while (false);


        while(num>1000){
            System.out.println("Body");

        }



    }

}

